
class sum
  {
	public static void main(String args[])
        {
         int n1=125, n2=24;
         int sum = n1 + n2;
         System.out.println(n1+" + "+n2 +" = "+sum);

         int diff = n1 - n2;
         System.out.println(n1+" - "+n2+" = "+diff);

        int mul = n1 * n2;
         System.out.println(n1+" * "+n2+" = "+mul);

        int div = n1 / n2;
         System.out.println(n1+" / "+n2+" = "+div);

         int mod = n1 % n2;
         System.out.println(n1+" mod "+n2+" = "+mod);
	}
  }


