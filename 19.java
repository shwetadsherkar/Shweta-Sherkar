 class pawer 
{
    public static void main(String[] args) 
    {
        
        int base = 3;

        int exponent = 4;

        int result = (int) Math.pow(base, exponent);

        
        System.out.println(base + " raised to the power " + exponent + " is " + result);
    }
}

